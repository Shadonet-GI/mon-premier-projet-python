# fonctions contenants les opérations modulaires
import math
# def permet de definir une fonction et addition est le nom de la fonction et dans les parentheses e sont les argumentsou les parametres
def addition(a,b):
    return a+b
def soustraction(a,b):
    return a-b
def multiplication(a,b):
    return a*b
def division(a,b):
    return a/b
def puissance(a,b):
    return a**b
def racine(a):
    if a<0:
        return "erreure racine carrée negative"
    return math.sqrt(a)

        
    
    
    
    
        